static int linenoiseEdit(int stdin_fd, int stdout_fd, char *buf, size_t buflen, const char *prompt)
{
    struct linenoiseState l;



    l.ifd = stdin_fd;
    l.ofd = stdout_fd;
    l.buf = buf;
    l.buflen = buflen;
    l.prompt = prompt;
    l.plen = strlen(prompt);
    l.oldpos = l.pos = 0;
    l.len = 0;
    l.cols = getColumns(stdin_fd, stdout_fd);
    l.maxrows = 0;
    l.history_index = 0;


    l.buf[0] = '\0';
    l.buflen--;



    linenoiseHistoryAdd("");

    if (write(l.ofd,prompt,l.plen) == -1) return -1;
    while(1) {
        char c;
        int nread;
        char seq[3];

        nread = read(l.ifd,&c,1);
        if (nread <= 0) return l.len;




        if (c == 9 && completionCallback != ((void *)0)) {
            c = completeLine(&l);

            if (c < 0) return l.len;

            if (c == 0) continue;
        }

        switch(c) {
        case ENTER:
            history_len--;
            free(history[history_len]);
            if (mlmode) linenoiseEditMoveEnd(&l);
            if (hintsCallback) {


                linenoiseHintsCallback *hc = hintsCallback;
                hintsCallback = ((void *)0);
                refreshLine(&l);
                hintsCallback = hc;
            }
            return (int)l.len;
        case CTRL_C:
            (*__errno_location ()) = 11;
            return -1;
        case BACKSPACE:
        case 8:
            linenoiseEditBackspace(&l);
            break;
        case CTRL_D:

            if (l.len > 0) {
                linenoiseEditDelete(&l);
            } else {
                history_len--;
                free(history[history_len]);
                return -1;
            }
            break;
        case CTRL_T:
            if (l.pos > 0 && l.pos < l.len) {
                int aux = buf[l.pos-1];
                buf[l.pos-1] = buf[l.pos];
                buf[l.pos] = aux;
                if (l.pos != l.len-1) l.pos++;
                refreshLine(&l);
            }
            break;
        case CTRL_B:
            linenoiseEditMoveLeft(&l);
            break;
        case CTRL_F:
            linenoiseEditMoveRight(&l);
            break;
        case CTRL_P:
            linenoiseEditHistoryNext(&l, 1);
            break;
        case CTRL_N:
            linenoiseEditHistoryNext(&l, 0);
            break;
        case ESC:



            if (read(l.ifd,seq,1) == -1) break;
            if (read(l.ifd,seq+1,1) == -1) break;


            if (seq[0] == '[') {
                if (seq[1] >= '0' && seq[1] <= '9') {

                    if (read(l.ifd,seq+2,1) == -1) break;
                    if (seq[2] == '~') {
                        switch(seq[1]) {
                        case '3':
                            linenoiseEditDelete(&l);
                            break;
                        }
                    }
                } else {
                    switch(seq[1]) {
                    case 'A':
                        linenoiseEditHistoryNext(&l, 1);
                        break;
                    case 'B':
                        linenoiseEditHistoryNext(&l, 0);
                        break;
                    case 'C':
                        linenoiseEditMoveRight(&l);
                        break;
                    case 'D':
                        linenoiseEditMoveLeft(&l);
                        break;
                    case 'H':
                        linenoiseEditMoveHome(&l);
                        break;
                    case 'F':
                        linenoiseEditMoveEnd(&l);
                        break;
                    }
                }
            }


            else if (seq[0] == 'O') {
                switch(seq[1]) {
                case 'H':
                    linenoiseEditMoveHome(&l);
                    break;
                case 'F':
                    linenoiseEditMoveEnd(&l);
                    break;
                }
            }
            break;
        default:
            if (linenoiseEditInsert(&l,c)) return -1;
            break;
        case CTRL_U:
            buf[0] = '\0';
            l.pos = l.len = 0;
            refreshLine(&l);
            break;
        case CTRL_K:
            buf[l.pos] = '\0';
            l.len = l.pos;
            refreshLine(&l);
            break;
        case CTRL_A:
            linenoiseEditMoveHome(&l);
            break;
        case CTRL_E:
            linenoiseEditMoveEnd(&l);
            break;
        case CTRL_L:
            linenoiseClearScreen();
            refreshLine(&l);
            break;
        case CTRL_W:
            linenoiseEditDeletePrevWord(&l);
            break;
        }
    }
    return l.len;
}




